    // Orchestrates the main script logic, including redirects and UI interactions.
    // ============================================================================

    /**
     * MainController class for running the script logic.
     */
    class MainController {
        static config = ConfigManager.getConfig();
        static waitTimes = ConfigManager.getWaitTimes();

        /**
         * Initializes the script by loading classifications and running the main logic.
         */
        static async init() {
            await ConfigManager.loadClassifications();
            ConfigManager.registerMenuCommands();

            if (document.readyState === 'loading') {
                document.addEventListener('DOMContentLoaded', () => this.runScript(), { once: true });
            } else {
                this.runScript();
            }
        }

        /**
         * Main script execution logic.
         */
        static runScript() {
            try {
                const urlParams = new URLSearchParams(window.location.search);
                const currentDomain = window.location.hostname;
                const category = ConfigManager.getDomainCategory(currentDomain);
                let waitTime = this.config.advancedMode ? (this.waitTimes[category] || this.waitTimes.default) : this.config.globalTime;
                waitTime = Utils.randomizeWaitTime(waitTime);

                if (Utils.isBypassSite()) {
                    const targetUrl = urlParams.get('url');
                    if (targetUrl) UIManager.injectBypassInfoUI(decodeURIComponent(targetUrl));
                    return;
                }

                const rawRedirect = urlParams.get('redirect');

                if (!rawRedirect) {
                    if (!this.config.stealthMode) {
                        UIManager.showBypassToast();
                    }
                    const randomDelay = this.config.stealthMode ? Math.random() * 2000 + 500 : 800;
                    setTimeout(() => {
                        const targetUrl = `https://bypass.vip/userscript.html?url=${encodeURIComponent(location.href)}&time=${waitTime}&key=${this.config.key}&safe=${this.config.safeMode}&rnd=${Math.random().toString(36).substr(2, 9)}`;
                        location.replace(targetUrl);
                    }, randomDelay);
                    return;
                }

                let redirectUrl = rawRedirect;

                if (!Utils.isValidUrl(redirectUrl)) {
                    try {
                        const decoded = decodeURIComponent(rawRedirect);
                        if (Utils.isValidUrl(decoded)) {
                            redirectUrl = decoded;
                        } else {
                            throw new Error('Invalid redirect URL after decoding');
                        }
                    } catch (err) {
                        ErrorHandler.showCriticalError('Error: Invalid or malformed redirect URL. Please try again.');
                        return;
                    }
                }

                if (document.body && document.body.hasAttribute('data-bypass-injected')) {
                    return;
                }

                UIManager.showBypassToast();

                const container = UIManager.createContainer(this.config, this.waitTimes);
                if (document.body) {
                    document.body.appendChild(container);
                    document.body.setAttribute('data-bypass-injected', 'true');
                } else {
                    document.documentElement.appendChild(container);
                    document.documentElement.setAttribute('data-bypass-injected', 'true');
                }

                // Semi-hide UI if captcha is detected
                if (Utils.hasCaptcha()) {
                    container.style.opacity = '0.5';
                    container.style.pointerEvents = 'none';
                }
                setTimeout(() => {
                    if (Utils.hasCaptcha()) {
                        container.style.opacity = '0.5';
                        container.style.pointerEvents = 'none';
                    }
                }, 2000);

                // Cache DOM elements for performance
                const settingsBtn = container.querySelector(`#${UIManager.ids.settingsBtn}`);
                const settingsDropdown = container.querySelector(`#${UIManager.ids.settingsDropdown}`);
                const saveSettings = container.querySelector(`#${UIManager.ids.saveSettings}`);
                const cancelBtn = container.querySelector(`#${UIManager.ids.cancelBtn}`);
                const countdownEl = container.querySelector(`#${UIManager.ids.countdown}`);
                const btn = container.querySelector(`#${UIManager.ids.nextBtn}`);
                const spinner = container.querySelector(`#${UIManager.ids.spinner}`);

                // Settings dropdown toggle
                settingsBtn.addEventListener('click', () => {
                    settingsDropdown.style.display = settingsDropdown.style.display === 'none' ? 'block' : 'none';
                });

                // Save settings
                saveSettings.addEventListener('click', () => {
                    const advancedMode = document.getElementById(UIManager.ids.advancedModeInput).checked;
                    const stealthMode = document.getElementById(UIManager.ids.stealthModeInput).checked;
                    const theme = document.getElementById(UIManager.ids.themeInput).checked ? 'light' : 'dark';
                    const autoProceed = document.getElementById(UIManager.ids.autoProceedInput).checked;
                    const globalTime = parseInt(document.getElementById(UIManager.ids.timeInput).value);
                    const key = document.getElementById(UIManager.ids.keyInput).value;
                    const waitTimesNew = {};
                    for (const cat of Object.keys(ConfigManager.DEFAULT_WAIT_TIMES)) {
                        const val = parseInt(document.getElementById(UIManager.timeIdMap[cat]).value);
                        waitTimesNew[cat] = isNaN(val) ? ConfigManager.DEFAULT_WAIT_TIMES[cat] : val;
                    }
                    ConfigManager.saveConfig({ advancedMode, stealthMode, theme, autoProceed, globalTime, key, safeMode: this.config.safeMode });
                    ConfigManager.saveWaitTimes(waitTimesNew);
                    settingsDropdown.style.display = 'none';
                    alert('Settings saved. Reload the page to apply changes.');
                });

                // Cancel button
                cancelBtn.addEventListener('click', () => {
                    if (confirm('Are you sure you want to cancel and reload?')) {
                        location.reload();
                    }
                });

                // Countdown and hash handling
                if (Utils.hasHash(redirectUrl)) {
                    let time = 8;
                    countdownEl.style.color = '#ff4d4d';
                    countdownEl.style.fontWeight = 'bold';
                    const interval = setInterval(() => {
                        countdownEl.textContent = `YOU HAVE EXACTLY ${time} SECONDS TO CLICK THE BUTTON BEFORE YOUR HASH EXPIRES`;
                        time--;
                        if (time < 0) {
                            clearInterval(interval);
                            countdownEl.textContent = 'HASH EXPIRED. RETRYING...';
                            countdownEl.style.color = '';
                            countdownEl.style.fontWeight = '';
                            btn.disabled = true;
                            spinner.style.display = 'block';
                            setTimeout(() => {
                                location.replace(location.href.split('?')[0]);
                            }, 3500);
                        }
                    }, 1000);
                } else {
                    countdownEl.style.display = 'none';
                }

                // Proceed button logic
                const performRedirect = () => {
                    if (!redirectUrl || btn.disabled) return;
                    try {
                        btn.disabled = true;
                        spinner.style.display = 'block';
                        const randomDelay = Math.random() * 200 + 60;
                        setTimeout(() => {
                            try {
                                window.location.assign(redirectUrl);
                            } catch (err) {
                                window.location.href = redirectUrl;
                            }
                        }, randomDelay);
                    } catch (err) {
                        ErrorHandler.handleRedirectFailure(redirectUrl);
                        btn.disabled = false;
                        spinner.style.display = 'none';
                    }
                };

                btn.addEventListener('click', (e) => {
                    e.preventDefault();
                    e.stopPropagation();
                    performRedirect();
                }, { passive: false });

                btn.addEventListener('touchend', (e) => {
                    e.preventDefault();
                    e.stopPropagation();
                    performRedirect();
                }, { passive: false });

                // Auto-proceed if enabled and no hash
                if (this.config.autoProceed && !Utils.hasHash(redirectUrl)) {
                    setTimeout(() => {
                        if (!btn.disabled) performRedirect();
                    }, waitTime * 1000);
                }

                // Accessibility
                try {
                    btn.setAttribute('aria-label', 'Proceed to link');
                    btn.tabIndex = 0;
                } catch (err) { /* silent */ }

            } catch (err) {
                ErrorHandler.showCriticalError(`Error in bypass script: ${err && err.message ? err.message : err}. Please reload the page.`);
            }
        }
    }

